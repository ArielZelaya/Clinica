    private void consultaInicial() {
try { 
String sentenciaSql = "select codigo_paciente,nombre,telefono,fecha from cita  natural join paciente ";
java.sql.Statement statement = this.conexion.createStatement();
ResultSet resultado = statement.executeQuery(sentenciaSql);
while (resultado.next()) {
Citas cita = new Citas();
cita.codigo = resultado.getString("codigo_paciente");
cita.paciente = resultado.getString("nombre");
cita.tel=resultado.getString("telefono");
cita.fecha=resultado.getString("fecha");

this.citaTModel.citas.add(cita);
}
TableCitas.repaint();
} catch (SQLException ex) {
JOptionPane.showMessageDialog(this, "Error al recuperar los productos de la base de datos");
ex.printStackTrace();
}
}
